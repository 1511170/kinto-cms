import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const indexPath = process.env.SEARCH_INDEX_PATH
  ? path.resolve(process.env.SEARCH_INDEX_PATH)
  : path.join(root, 'dist', 'search-index.json');
const outPath = path.join(root, 'worker', 'generated', 'product-redirects.ts');

function slugify(input) {
  return String(input || '')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/&/g, ' y ')
    .replace(/\+/g, ' plus ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-{2,}/g, '-');
}

function variants(product) {
  const out = new Set();
  const handle = slugify(product.h);
  const title = slugify(product.t);
  const sku = slugify(product.sku);
  const brand = slugify(product.b);
  const category = slugify(product.c);

  for (const s of [handle, title, sku && !/^\d+$/.test(sku) ? sku : '', `${title}-${sku}`, `${brand}-${title}`, `${category}-${title}`]) {
    if (s) out.add(s);
  }

  // Common mojibake / legacy slug variants observed in imported data.
  const bases = [...out];
  for (const s of bases) {
    out.add(s.replace(/\bcaa\b/g, 'cana').replace(/caa-/g, 'cana-'));
    out.add(s.replace(/\bseuelo\b/g, 'senuelo').replace(/seuelo-/g, 'senuelo-'));
    out.add(s.replace(/\bsenuel0\b/g, 'senuelo').replace(/senuel0-/g, 'senuelo-'));
    out.add(s.replace(/\bmolnete\b/g, 'molinete').replace(/molnete-/g, 'molinete-'));
  }

  return [...out].filter(Boolean);
}

if (!fs.existsSync(indexPath)) {
  throw new Error(`No existe ${indexPath}. Ejecuta astro build primero.`);
}

const products = JSON.parse(fs.readFileSync(indexPath, 'utf8'));
if (!Array.isArray(products) || products.length < 1000) {
  throw new Error(`Índice de productos inválido o incompleto (${Array.isArray(products) ? products.length : 'no-array'} items): ${indexPath}`);
}
const candidates = new Map();
const collisions = new Set();

for (const product of products) {
  const target = slugify(product.h);
  if (!target) continue;
  for (const alt of variants(product)) {
    if (!alt || alt === target) continue;
    if (candidates.has(alt) && candidates.get(alt) !== target) {
      collisions.add(alt);
      continue;
    }
    candidates.set(alt, target);
  }
}

for (const key of collisions) candidates.delete(key);

const entries = [...candidates.entries()].sort(([a], [b]) => a.localeCompare(b));
const handles = [...new Set(products.map((product) => slugify(product.h)).filter(Boolean))].sort();
const body = `// Auto-generated by scripts/generate-product-redirects.mjs after \`npm run build\`.\n// Maps legacy WooCommerce/product slugs to current Shopify handles.\nexport const PRODUCT_REDIRECTS: Record<string, string> = ${JSON.stringify(Object.fromEntries(entries), null, 2)};\n\n// Current Shopify product handles. Used by legacy redirects to avoid 301s to missing PDPs.\nexport const PRODUCT_HANDLES: Record<string, true> = ${JSON.stringify(Object.fromEntries(handles.map((handle) => [handle, true])), null, 2)};\n`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, body);
console.log(`Generated ${entries.length} product redirect aliases at ${path.relative(root, outPath)} (${collisions.size} ambiguous aliases skipped).`);
